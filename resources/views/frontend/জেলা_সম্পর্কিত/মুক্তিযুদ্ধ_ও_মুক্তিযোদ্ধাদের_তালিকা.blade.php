        <p><strong>মেহেরপুর জেলার শহীদ মুক্তিযোদ্ধাদের তালিকা</strong></p>

        <p>১. খোন্দকার আবদুর রশিদ, পিতা: ড. আবদুর রহমান খন্দকার, বামনপাড়া, মেহেরপুর।</p>

        <p>২. মোঃ ফজলুর রহমান, পিতা: নাসির উদ্দিন, স্টেডিয়ামপাড়া, মেহেরপুর।</p>

        <p>৩. শাকাতুল্লাহ, পিতা: মোঃ আফেল উদ্দিন, গাড়াবাড়িয়া, গাংনী, মেহেরপুর।</p>

        <p>৪. মোঃ মনসুর আলী, পিতা: শামসের আলী, বন্দর, মেহেরপুর।</p>

        <p>৫. মহসিন আলী, পিতা: ভাদু মন্ডল, মোনাখালী, মেহেরপুর।</p>

        <p>৬. মহরম আলী, পিতা: আয়েন উদ্দিন, শোলমারি, মেহেরপুর।</p>

        <p>৭. ইউনুস আলী, শালিখা, মেহেরপুর।</p>

        <p>৮. রমজান আলী খান, পিতা: বেঙ্গু খান, রতনপুর, মেহেরপুর।</p>

        <p>৯. আলীম উদ্দিন মন্ডল, দিঘিরপাড়া, মেহেরপুর।</p>

        <p>১০. রফিকুল ইসলাম, পিতা: মনসুর আহমেদ, মেহেরপুর।</p>

        <p>১১. আবদুল জব্বার, পিতা-মাহাতাব উদ্দিন বিশ্বাস, আমঝুপি, মেহেরপুর।</p>

        <p>১২. নাসির উদ্দিন, পিতা: আয়েন উদ্দিন জোয়াদ্দার, মদনাডাঙ্গা, মেহেরপুর।</p>

        <p>১৩. জালাল হোসেন, পিতা: কালু মন্ডল, বড়শিবাড়িয়া, মেহেরপুর।</p>

        <p>১৪. হারেজ উদ্দিন, পিতা: তুষ্ট মন্ডল, চৌগাছা, গাংনী, মেহেরপুর।</p>

        <p>১৫. আবদুস সালাম, পিতা: আবদুল হালিম মিয়া, পিরোজপুর, মেহেরপুর।</p>

        <p>১৬. সাদেক আলী, পিতা-হোসেন আলী, উজলপুর, মেহেরপুর।</p>

        <p>১৭. নুরুল ইসলাম, পিতা: মকছেদ আলী, রাজাপুর, মেহেরপুর।</p>

        <p>১৮. আসগর হোসেন, পিতা: গয়জদ্দিন, আমঝুপি, মেহেরপুর।</p>

        <p>১৯. খায়েরউদ্দিন, পিতা: নছর শেখ, রাজনগর, মেহেরপুর।</p>

        <p>২০. নাজিম উদ্দিন, পিতা: মহিউদ্দিন, হাসনাবাদ কালোনী, মেহেরপুর।</p>

        <p>২১. ওলিউল হক, পিতা: কানাই শেখ, মোনাখালী, মেহেরপুর।</p>

        <p>২২. ঈমান আলী, পিতা: মুন্সি মিনাজ উদ্দিন, বুড়িপোতা, মেহেরপুর।</p>

        <p>২৩. পাঞ্জাব আলী, পিতা: খোকা শেখ, কাজিপুর, গাংনী, মেহেরপুর।</p>

        <p>২৪. লোকমান হোসেন, পিতা: ইকাব মন্ডল, পিরোজপুর, মেহেরপুর।</p>

        <p>২৫. আরজ আলী, মেহেরপুর।</p>

        <p>২৬. তাহাজ উদ্দিন, পিতা: রমজান শেখ, কোমরপুর, মেহেরপুর।</p>

        <p>২৭. আফাজ উদ্দিন, পিতা: রমজান শেখ, কোমরপুর, মেহেরপুর।</p>

        <p>২৮. আকবর আলী খান (আকুল), পিতা: রহমত খান, পিরোজপুর, মেহেরপুর।</p>

        <p>২৯. আবদুল হামিদ, পিতা: আবদুর রশিদ, মল্লিকপাড়া, মেহেরপুর।</p>

        <p>৩০. &nbsp;খবির উদ্দিন, পিতা: রহিম বক্স, রাধাগোবিন্দপুর, গাংনী, মেহেরপুর।</p>

        <p>৩১. সিপাহী রহিতুল্লাহ, রহিমবক্স, রাধাগোবিন্দপুর, গাংনী, মেহেরপুর।</p>

        <p>৩২. ওমর আলী শাহ, পিতা: খোকন শাহ, তেরাইল, গাংনী, মেহেরপুর।</p>

        <p>৩৩. আবদুল মান্নান, পিতা: হেকমত আলী, কুতুবপুর, মেহেরপুর।</p>

        <p>৩৪. আবদুস সাত্তার, পিতা: জাফর মন্ডল, পিরতলা, গাংনী, মেহেরপুর।</p>

        <p>৩৫. আবুল কাশেম, পিতা: ফজল শেখ, গোভীপুর, মেহেরপুর।</p>

        <p>৩৬. আবদুল জলিল, পিতা: খবির উদ্দিন, চৌগাছা, গাংনী, মেহেরপুর।</p>

        <p>৩৭. নায়েক সুবেদার আবুল কাশেম, (ই.পি.আর. সদস্য)</p>

        <p>৩৮. হাবিলদার আবদুস সালাম (ই.পি.আর সদস্য)</p>

        <p>৩৯. সেপাই আসাদুজ্জামান, (ই.পি.আর.সদস্য)</p>

        <p>৪০. চিত্ত মল্লিক, পিতা: ধীরেন্দ্র মল্লিক, বল্লভপুর, মেহেরপুর।</p>

        <p>৪১. ইলিয়াস খান, পিতা: মোবারক হোসেন খান, মেহেরপুর।</p>

        <p>৪২. আবুল হোসেন, পিতা: পাঁচু শেখ, মেহেরপুর।</p>

        <p>৪৩. আয়াতুল্লাহ মল্লিক, পিতা: আধর মল্লিক, আনন্দবাস, মেহেরপুর।</p>

        <p>৪৪. সিরাজুল ইসলাম, পিতা: রব্বানীমন্ডল, মোনাখালী, মেহেরপুর।</p>

        <p>৪৫. আফছার আলী, পিতা: দেলবার মন্ডল, পিরোজপুর, মেহেরপুর।</p>

        <p>৪৬. একরামুলহক (ছোট), পিতা: হাফেজ উদ্দিন বিশ্বাস, চৌগাছা, গাংনী, মেহেরপুর।</p>

        <p>৪৭. আরশাদ আলী, পিতা: লুৎফর রহমান, আনন্দবাস, মেহেরপুর।</p>

        <p>৪৮. খায়রুদ্দিন, পিতা: কছিমন্দিন, পিরোজপুর, মেহেরপুর।</p>

        <p>৪৯. মোক্তার ইসলাম, পিতা: হোসেন আলী, সোনাপুর, মেহেরপুর।</p>

        <p>৫০. আমিনুল ইসলাম গাজী, পিতা: ইংরাজ মোল্লা, মল্লিক পাড়া, মেহেরপুর।</p>

        <p>৫১. সিরাজুল ইসলাম, পিতা: খেলাফত আল, বন্দর, মেহেরপুর।</p>

