        <p>লাল মোলামের ঢেঁকি কুসুম কাঠের পোয়া</p>

        <p>ভাসুর যদি তেমন হয় ছেমায় ঢেঁকি পেতে দেয়।’’</p>

        <p><strong>(জ)</strong><strong>শারী গানঃ</strong></p>

        <p>এই গান হচ্ছে কর্ম সংগীত। মেহেরপুর অঞ্চলে শারীগানের প্রভাব আজও বিদ্যমান। পাকা ঘরের জলছাদ পেটানো কিংবা কোন ভারী কাজের জোশ সৃষ্টির জন্য শারীগান গাওয়া হয়ে থাকে।</p>

        <p><strong>(ঝ)</strong><strong>জারী গানঃ</strong></p>

        <p>জারীগান মূলতঃ ইসলামী দৃষ্টিকোন থেকে সৃষ্টি। মেহেরপুর অঞ্চলে মহররমের সময় গায়করা দলবদ্ধ হয়ে বাড়ী বাড়ী জারীগান গেয়ে বেড়ায়। যাদবপুরের বেলাল হোসেন বয়াতী জারীগানের গায়ক হিসেবে সুপ্রতিষ্ঠিত। এছাড়াও অনেক জারীগায়ক দল এ অঞ্চলে রয়েছে। জারীগানের কিছু অংশ তুলে ধরা হলোঃ-</p>

        <p>‘‘ঈমান যে না আনিবে মক্কার উপরে</p>

        <p>গোনাগার হয়ে যাবে দোযখ মাঝারে</p>

        <p>আরে রোমের ও শহরে ছিল</p>

        <p>ইব্রাহীম পায়গম্বর</p>

        <p>বহুদিবস বাদশাহী করে এই দুনিয়ার পর।’’</p>

