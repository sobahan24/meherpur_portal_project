        <p><strong>(ঘ)</strong><strong>ভাসান গানঃ</strong></p>

        <p>মেহেরপুরের বিভিন্ন গ্রামে ভাসান গানের দল রয়েছে। এই গানের বৈশিষ্ট হলো- তিনটি পালা করে গায়করা গান গেয়ে থাকেন। জন্মপালা, বাঁচার পালা ও মৃত্যুপালা। মৃত্যুপালা হচ্ছে শ্রোতাদের কাছে সবচেয়ে প্রিয়। শীতকালে বাড়ী বাড়ী ভাসান গানের আসর বরে থাকে। সারা রাত ধরে এ গানের আসর চলে থাকে। ভাসান গানের মধ্যে বেহুলা স্বামী বিয়োগের ঘটনাকে কেন্দ্র করে যে বর্ণনা দেয়া হয়ে থাকে তা শ্রোতার মনকে বিশেষভাবে আলোড়িত করে থাকে। ভাসান গানের কিছু্ অংশ এখানে তুলে ধরা হলোঃ-</p>

        <p>‘‘ওকি সাধ আছে হে দিতে লকায়ের বিয়ে</p>

        <p>আর কিছু দিন রাখবো ঘরে ধুলো খেলা দিয়ে।’’</p>

        <p><strong>(ঙ)</strong><strong>বিয়ের গানঃ</strong></p>

        <p>লোক সংস্কৃতির ভান্ডার অফুরন্ত। প্রতিনিয়ত এর উপাদান বৃদ্ধি পেয়ে চলেছে। আধুনিকতার উষ্ণ আবেদনের প্রেক্ষিতে অতীতের অনেক লোকসংস্কৃতি হারিয়ে গেছে। তবে মেহেরপুরের গ্রামগুলোতে অশিক্ষিত মেয়েরা বিয়ের গানের শত শত পংক্তি অনর্গল মুখস্থ বলে যেতে পারে। এ সমসত্ম গান তাঁরা নিজেরাই সৃষ্টি করেন। মেহেরপুরের গ্রামাঞ্চলের প্রচলিত বিয়ের গান, ঢেঁকি মোঙ্গলানোর গান, ক্ষীর খাওয়ানোর গান এখানে তুলে ধরা হলোঃ-</p>

        <p>বিয়ের গানঃ ‘‘দুলাভাই গিয়েছে শহরে, আনবে নাকের নথরে</p>

        <p>সেই নথ নাকে দিয়ে নাক ঘুরিয়ে নাচবো রে।’’</p>

        <p><strong>(চ)</strong><strong>ক্ষীর খাওয়ানার গানঃ</strong></p>

        <p>‘‘আলুয়ার চালে কাঞ্চন দুধে ক্ষীরোয়া পাকালাম</p>

        <p>সেই না ক্ষীরোয়া খেতে গরমি লেগেছে।</p>

        <p>কোথায় আছ বড়ভাবী পাক্কা হিলোয়ররে।’’</p>

