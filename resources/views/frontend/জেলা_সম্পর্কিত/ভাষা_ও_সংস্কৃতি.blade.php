        <p>মেহেরপুর জেলা অবিভক্ত নদীয়া জেলার অন্যতম প্রাচীন ঐতিহ্যবাহী একটি জেলা। নদীয়া জেলা অবিভক্ত বাংলার সাংস্কৃতিক কর্মকান্ডের প্রাণকেন্দ্র ছিল। মেহেরপুর জেলার ভাষা ও লোক সংস্কৃতির কিছু উদাহরণ নিম্নে তুলে ধরা হলোঃ</p>

        <p><strong>মেহেরপুরের লোকসংস্কৃতিঃ</strong></p>

        <p>১৯৪৭ সালে দেশ বিভাগের পর কুষ্টিয়ার লালন শাহের লালনগীতির ব্যাপক চর্চার প্রভাব মেহেরপুরের সাংস্কৃতিক অঙ্গনকে প্রভাবিত করেছে। এছাড়া মেহেরপুরের লোক-সংস্কৃতি, বাউলগীতি, আঞ্চলিক গীতি, নাট্যচর্চা, ভাসানগান ও মানিকপীরের গান উল্লেখযোগ্য।</p>

        <p>মেহেরপুর নদীয়ার প্রাচীন জনপদ হওয়ায় এখানে লোকসংস্কৃতি বা গ্রামীণ সংস্কৃতি বিভিন্নভাবে চর্চার মাধ্যমে ঐতিহ্যে রূপান্তরিত হয়েছে। বাঙালীর সমাজ জীবনে নানা উৎসব আয়োজনে নানা ধরণের গীত, কবিগান, ভাবগান, পুঁথিপাঠ, মেঠো গান, মানসার গান, ভাসান গান, ছেলে নাচানো গান, মানিকপীরের গান, বোলান গান, অষ্টগান, গাজীর গীত ও কৃষ্ণগান সবিশেষ উল্লেখযোগ্য। লোক সংস্কৃতির বিকাশের ক্ষেত্রে ধর্মীয় প্রভাব ও এক শ্রেণীর প্রভাবশালী সমাজপতিদের বিদ্রুপান্তক দৃষ্টিভঙ্গি বিশেষ অন্তরায় সৃষ্টি করলেও বহুকাল ধরেই লোকসংস্কৃতি মেহেরপুরের সমাজজীবনে নানাভাবে বিদ্যমান রয়েছে।</p>

        <p><strong>(ক) </strong><strong>বাউলগীতিঃ</strong></p>

        <p>বৃহত্তর কুষ্টিয়ার অঞ্চল হিসেবে মেহেরপুরে কুষ্টিয়ার বাউল সম্রাট লালন শাহের প্রভাবে হাজারো বাউল অনুরাগী ও বাউল শিল্পীর সৃষ্টি হয়েছে। তাঁরা মানবধর্মের কথা বলেন এবং দেহতাত্ত্বিক ব্যাখ্যা প্রদানের মাধ্যমে স্রষ্টার সাথে সৃষ্টির সম্পর্ক ব্যাখ্যা করেন। বাউলগণ দেহতাত্ত্বিক বিশ্লেষণ ও মরমী গানের মাধ্যমে মানুষকে অতিন্দ্রীয়লোকে বিচরণ করাতে সক্ষম হন। মেহেরপুরে এখনো শত শত বাউল অনুরাগী তাঁদের ব্যক্তিজীবনে বাউল সঙ্গীত ও লালনের জীবনাদর্শ চর্চা অব্যাহত রেখেছেন।</p>

        <p><strong>(খ)</strong><strong>কলিমউদ্দিন শাহ এর আখড়াঃ</strong></p>

        <p>মেহেরপুরের ভৈরব নদের পশ্চিম তীরে যাদবপুরের গ্রামে জন্ম নেয়া কলিমউদ্দীন শাহ একজন বিখ্যাত বাউল সাধক। এ গ্রামে ১৪ বিঘা জমির উপর রয়েছে ‘কলিমউদ্দীন শাহের আখড়া’- যা এলাকায় ‘কালি ফকিরের আখড়া’ নামে পরিচিত। প্রতি বছর ১৪ই ভাদ্র, ২৭শে চৈত্র ও ৩০শে ফাল্গুন এ তিনটি দিবসে এখানে দেশ-বিদেশের বাউল ফকিরগণের সমাবেশ হয়। নিঃসন্তান অবস্থায় বাংলা ১৩৯১ সনের ২৭শে চৈত্র কলিমউদ্দীন শাহ ওরফে কালি ফকির মারা যান। তাঁর নির্মিত এ আখড়ার কেন্দ্রস্থলেই তাকে সমাহিত করা হয়।</p>

        <p><strong>(গ)</strong><strong>মানিকপীরের গানঃ</strong></p>

        <p>মানিক পীর মুসলমান ও হিন্দু উভয় সম্প্রদায়ের গৃহস্থের নিকট সমভাবে শ্রদ্ধার পাত্র হয়ে আছেন।। মেহেরপুরের লোকসংস্কৃতির ঐতিহ্যে মানিকপীরের গান এক অমূল্য সম্পদ হিসেবে স্বীকৃত। মেহেরপুরের গ্রামাঞ্চলে বিশেষ করে বুড়িপোতা ও পিরোজপুর ইউনিয়নের গ্রামগুলোতে মানিকপীরের গানের বেশ প্রচলন রয়েছে। পৌষ মাসের সংক্রান্তিতে মানিকপীরের গান গেয়ে কেউ কেউ ভিক্ষা করে বেড়ায়। ভিক্ষার দ্রব্যসামগ্রী দিয়ে ১লা মাঘ একটি নির্দিষ্ট স্থানে রান্না করে তবারক হিসেবে বিলি করা হয়।</p>

        <p>মেহেরপুর অঞ্চলে প্রচলিত মানিকপীরের গানের কিছু অংশ এখানে তুলে ধরা হলোঃ-</p>

